	private void btn_saveMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_btn_saveMouseClicked

		String id = txt_id.getText();
		String name = txt_name.getText();
		String surname = txt_surname.getText();
		String phoneNumber = txt_phoneNumber.getText();
		String address = txt_address.getText();

		String fields[] = { id, name, surname, phoneNumber, address };

		int fieldCheckout = 0;
		Boolean isDuplicated = false;

		for (String field : fields) {
			if (!field.isEmpty()) {
				fieldCheckout++;
			}
		}

		if (fieldCheckout == 5) {

			List<Contact> contacts = JSONReader.GetContacts();
			for (Contact contact : contacts) {
				// System.out.println("JSON id: " + contact.getId() + " txt id: " + id + " duplicated: " + isDuplicated);

				if (id.equals(contact.getId())) {
					JOptionPane.showMessageDialog(this,
							"la identificación " + id + " ya está registrada como contacto.", "ERROR",
							JOptionPane.ERROR_MESSAGE);
					isDuplicated = true;
					break;
				}
				else {
					isDuplicated = false;
				}

			}

			if (!isDuplicated) {
				JSONReader.CreateNewContact(id, name, surname, phoneNumber, address);
				Contact newContact = new Contact(id, name, surname, phoneNumber, address);
				contacts.add(newContact);
				JSONReader.SaveContacts(contacts);

				txt_id.setText("");
				txt_name.setText("");
				txt_surname.setText("");
				txt_phoneNumber.setText("");
				txt_address.setText("");
	
				txt_id.requestFocusInWindow();

				JOptionPane.showMessageDialog(this, "El contacto ha sido creado", "Información",JOptionPane.INFORMATION_MESSAGE);
			}



			

		} else {
			JOptionPane.showMessageDialog(this, "No pueden haber campos vacios en el formulario", "Atención",
					JOptionPane.ERROR_MESSAGE);
		}
	}// GEN-LAST:event_btn_saveMouseClicked

