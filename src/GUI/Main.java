	private void btn_seeDataMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_btn_seeDataMouseClicked

		List<Contact> contacts = JSONReader.GetContacts();

		// clear data in table
		model.setRowCount(0);

		if (!contacts.isEmpty()) {
			for (Contact contact : contacts) {
				model.addRow(new Object[] {
						contact.getId(),
						contact.getName(),
						contact.getSurname(),
						contact.getPhoneNumber(),
						contact.getAddress() });
			}
		} else {
			JOptionPane.showMessageDialog(this, "La agenda está vacia", getTitle(), JOptionPane.INFORMATION_MESSAGE);
		}

	}// GEN-LAST:event_btn_seeDataMouseClicked

